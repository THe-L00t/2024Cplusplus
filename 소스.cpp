#include <iostream>
#include <fstream>
#include <print>

#include "save.h"

int main()
{
	//[문제] 파일 "메인.cpp"에 사용된 알파벳 소문자의 개수를 출력하라
	//출력 예
	// a - 10
	// b - 2
	// 

	std::ifstream in{ "소스.cpp" };
	if (!in) { return 404; }

	char C;
	int c[26]{};
	while (in >> C) {
		for (int i = 'a'; i <='z'; ++i) {
			if (i == C) c[i - 'a'] += 1;
		}
	}
	for (int i = 0; i < 26; ++i) {
		std::cout << (char)(i + 'a') << "의 개수는 " << c[i] << std::endl;
	}

	(*save)("소스.cpp");//정석 함수 호출
}

